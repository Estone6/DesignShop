import axios from "axios";
import {
  GET_PRODUCTS_LIST,
  STORE_PRODUCTS_LIST,
  ERROR_PRODUCT_LIST,
  GET_PRODUCT_DETAILS,
  STORE_PRODUCT_DETAILS,
  ERROR_PRODUCT_DETAILS,
  PRODUCT_DELETE_REQUEST,
  PRODUCT_DELETE_SUCCESS,
  PRODUCT_DELETE_FAIL,
  PRODUCT_CREATE_REQUEST,
  PRODUCT_CREATE_SUCCESS,
  PRODUCT_CREATE_FAIL,
  PRODUCT_UPDATE_REQUEST,
  PRODUCT_UPDATE_SUCCESS,
  PRODUCT_UPDATE_FAIL,
} from "../constants/productsConstants";
import { logout } from "./userActions";

export const getProductsList = () => async (dispatch) => {
  try {
    dispatch({ type: GET_PRODUCTS_LIST });
    const { data } = await axios.get("/api/products");
    dispatch({ type: STORE_PRODUCTS_LIST, data: data });
  } catch (error) {
    dispatch({
      type: ERROR_PRODUCT_LIST,
      error:
        error.response && error.response.data.message
          ? error.response.data.message
          : error.message,
    });
  }
};

export const getProductDetails = (id) => async (dispatch) => {
  try {
    dispatch({ type: GET_PRODUCT_DETAILS });
    const { data } = await axios.get(`/api/products/${id}`);
    dispatch({ type: STORE_PRODUCT_DETAILS, data: data });
  } catch (error) {
    dispatch({
      type: ERROR_PRODUCT_DETAILS,
      error:
        error.response && error.response.data.message
          ? error.response.data.message
          : error.message,
    });
  }
};

export const deleteProduct = (id) => async (dispatch, getState) => {
  try {
    dispatch({
      type: PRODUCT_DELETE_REQUEST,
    });

    const {
      userLogin: { userInfo },
    } = getState();

    const config = {
      headers: {
        Authorization: `Bearer ${userInfo.token}`,
      },
    };

    await axios.delete(`/api/products/${id}`, config);

    dispatch({
      type: PRODUCT_DELETE_SUCCESS,
    });
  } catch (error) {
    const message =
      error.response && error.response.data.message
        ? error.response.data.message
        : error.message;
    if (message === "Not authorized, token failed") {
      dispatch(logout());
    }
    dispatch({
      type: PRODUCT_DELETE_FAIL,
      error: message,
    });
  }
};

